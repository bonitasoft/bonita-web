package org.bonitasoft.forms.server.api.impl.util;

/**
 * @author Vincent Elcrin
 * 
 */
public class WidgetExpressionEntry {

    private static final String EXPRESSION_KEY_SEPARATOR = ":";

    private final String entry;

    public WidgetExpressionEntry(String widgetId, ExpressionId expressionId) {
        assert widgetId != null;
        assert expressionId != null;

        entry = widgetId + EXPRESSION_KEY_SEPARATOR + expressionId;
    }

    @Override
    public String toString() {
        return entry;
    }

    @Override
    public int hashCode() {
        final int prime = 31;
        int result = 1;
        result = prime * result + ((entry == null) ? 0 : entry.hashCode());
        return result;
    }

    @Override
    public boolean equals(Object obj) {
        if (this == obj) {
            return true;
        }
        if (obj == null) {
            return false;
        }
        if (getClass() != obj.getClass()) {
            return false;
        }
        WidgetExpressionEntry other = (WidgetExpressionEntry) obj;
        if (entry == null) {
            if (other.entry != null) {
                return false;
            }
        } else if (!entry.equals(other.entry)) {
            return false;
        }
        return true;
    }

}
